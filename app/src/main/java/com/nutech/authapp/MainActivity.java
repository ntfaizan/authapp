    private void onClick(){
        binding.buttonPlus.setOnClickListener(v ->{
            counter++;
            binding.textViewCounter.setText(String.valueOf(counter));
        });
        binding.buttonMinus.setOnClickListener(v ->{
            counter--;
            binding.textViewCounter.setText(String.valueOf(counter));
        });
        binding.buttonReset.setOnClickListener(v ->{
            counter=0;
            binding.textViewCounter.setText(String.valueOf(counter));
        });
    }

